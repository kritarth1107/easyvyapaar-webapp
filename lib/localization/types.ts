type NestedKeyOf<T, Prefix extends string = ""> = T extends string
  ? Prefix extends ""
    ? never
    : Prefix
  : T extends Record<string, unknown>
    ? {
        [K in keyof T & string]: NestedKeyOf<
          T[K],
          Prefix extends "" ? K : `${Prefix}.${K}`
        >;
      }[keyof T & string]
    : never;

export type TranslationKey = NestedKeyOf<MessageTree>;
